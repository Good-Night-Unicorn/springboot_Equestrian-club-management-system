							this.ro.shebeileixing = true;
							continue;
						}
						if(o=='shebeitupian'){
							this.ruleForm.shebeitupian = obj[o];
							this.ro.shebeitupian = true;
							continue;
						}
						if(o=='shebeishuliang'){
							this.ruleForm.shebeishuliang = obj[o];
							this.ro.shebeishuliang = true;
							continue;
						}
						if(o=='shebeixinghao'){
							this.ruleForm.shebeixinghao = obj[o];
							this.ro.shebeixinghao = true;
							continue;
						}
						if(o=='shebeiyongtu'){
