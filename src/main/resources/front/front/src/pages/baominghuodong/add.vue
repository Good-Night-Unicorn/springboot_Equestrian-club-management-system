<script>
	export default {
		data() {
			return {
				id: '',
				baseUrl: '',
				ro:{
					huodongmingcheng : false,
					huodongleixing : false,
					fengmian : false,
					huodongrenshu : false,
					jifen : false,
					baomingshijian : false,
					huiyuanzhanghao : false,
					huiyuanxingming : false,
					jiaolianzhanghao : false,
					jiaolianxingming : false,
				},
				type: '',
				userTableName: localStorage.getItem('UserTableName'),
				ruleForm: {
					huodongmingcheng: '',
					huodongleixing: '',
					fengmian: '',
					huodongrenshu: '',
					jifen: '',
					baomingshijian: '',
					huiyuanzhanghao: '',
					huiyuanxingming: '',
					jiaolianzhanghao: '',
					jiaolianxingming: '',
				},
				huodongrenshuOptions: [],


				rules: {
					huodongmingcheng: [
					],
					huodongleixing: [
					],
					fengmian: [
					],
					huodongrenshu: [
						{ validator: this.$validate.isIntNumer, trigger: 'blur' },
					],
					jifen: [
						{ validator: this.$validate.isNumber, trigger: 'blur' },
					],
					baomingshijian: [
					],
					huiyuanzhanghao: [
					],
					huiyuanxingming: [
					],
					jiaolianzhanghao: [
					],
					jiaolianxingming: [
					],
				},
				centerType: false,
			};
		},
		computed: {



		},
		components: {
		},
		created() {
			if(this.$route.query.centerType){
				this.centerType = true
			}
			//this.bg();
			let type = this.$route.query.type ? this.$route.query.type : '';
			this.init(type);
			this.baseUrl = this.$config.baseUrl;
			this.ruleForm.baomingshijian = this.getCurDateTime()
		},
		methods: {
			getMakeZero(s) {
				return s < 10 ? '0' + s : s;
			},
			// 下载
			download(file){
				window.open(`${file}`)
			},
			// 初始化
			init(type) {
				this.type = type;
				if(type=='cross'){
					var obj = JSON.parse(localStorage.getItem('crossObj'));
					for (var o in obj){
						if(o=='huodongmingcheng'){
							this.ruleForm.huodongmingcheng = obj[o];
							this.ro.huodongmingcheng = true;
							continue;
						}
						if(o=='huodongleixing'){
							this.ruleForm.huodongleixing = obj[o];
							this.ro.huodongleixing = true;
							continue;
						}
						if(o=='fengmian'){
							this.ruleForm.fengmian = obj[o].split(",")[0];
							this.ro.fengmian = true;
							continue;
						}
						if(o=='huodongrenshu'){
							this.ruleForm.huodongrenshu = obj[o];
							this.ro.huodongrenshu = true;
							continue;
						}
						if(o=='jifen'){
							this.ruleForm.jifen = obj[o];
							this.ro.jifen = true;
							continue;
						}
						if(o=='baomingshijian'){
							this.ruleForm.baomingshijian = obj[o];
							this.ro.baomingshijian = true;
							continue;
						}
						if(o=='huiyuanzhanghao'){
							this.ruleForm.huiyuanzhanghao = obj[o];
							this.ro.huiyuanzhanghao = true;
							continue;
						}
						if(o=='huiyuanxingming'){
							this.ruleForm.huiyuanxingming = obj[o];
							this.ro.huiyuanxingming = true;
							continue;
						}
						if(o=='jiaolianzhanghao'){
							this.ruleForm.jiaolianzhanghao = obj[o];
							this.ro.jiaolianzhanghao = true;
							continue;
						}
						if(o=='jiaolianxingming'){
							this.ruleForm.jiaolianxingming = obj[o];
							this.ro.jiaolianxingming = true;
							continue;
						}
					}
					this.ruleForm.huodongrenshu = 0;
					this.ro.huodongrenshu = false;
				}else if(type=='edit'){
					this.info()
				}
				// 获取用户信息
				this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
					if (res.data.code == 0) {
						var json = res.data.data;
						if((json.huiyuanzhanghao!=''&&json.huiyuanzhanghao) || json.huiyuanzhanghao==0){
							this.ruleForm.huiyuanzhanghao = json.huiyuanzhanghao;
							this.ro.huiyuanzhanghao = true;
						}
						if((json.huiyuanxingming!=''&&json.huiyuanxingming) || json.huiyuanxingming==0){
							this.ruleForm.huiyuanxingming = json.huiyuanxingming;
							this.ro.huiyuanxingming = true;
						}
					}
				});
				this.huodongrenshuOptions = "1".split(',')

				if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
					localStorage.removeItem('raffleType')
					setTimeout(() => {
						this.onSubmit()
					}, 300)
				}
			},

			// 多级联动参数
			// 多级联动参数
			info() {
				this.$http.get(`baominghuodong/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
					if (res.data.code == 0) {
						this.ruleForm = res.data.data;
					}
				});
			},
			// 提交
			async onSubmit() {
				if(!this.ruleForm.huodongrenshu){
					this.$message.error("报名人数不能为空");
					return
				}
				var obj = JSON.parse(localStorage.getItem('crossObj'));
				var table = localStorage.getItem('crossTable');
				obj.huodongrenshu = obj.huodongrenshu - this.ruleForm.huodongrenshu
				if(obj.huodongrenshu<0){
					this.$message.error("报名人数不足");
					return
				}
				await this.$refs["ruleForm"].validate(async valid => {
					if(valid) {
						if(this.type=='cross'){
							var statusColumnName = localStorage.getItem('statusColumnName');
							var statusColumnValue = localStorage.getItem('statusColumnValue');
							if(statusColumnName && statusColumnName!='') {
								var obj = JSON.parse(localStorage.getItem('crossObj'));
								if(!statusColumnName.startsWith("[")) {
									for (var o in obj){
										if(o==statusColumnName){
											obj[o] = statusColumnValue;
										}
									}
									var table = localStorage.getItem('crossTable');
									await this.$http.post(table+'/update', obj).then(res => {});
								}
							}
						}
						// 跨表计算
						var obj = JSON.parse(localStorage.getItem('crossObj'));
						var table = localStorage.getItem('crossTable');

						obj.huodongrenshu = parseFloat(obj.huodongrenshu) - parseFloat(this.ruleForm.huodongrenshu)

						await this.$http.post(table+`/update`,obj).then(res => {});
						await this.$http.post(`baominghuodong/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(async res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
										
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				});
			},
			// 获取uuid
			getUUID () {
				return new Date().getTime();
			},
			// 返回
			back() {
				this.$router.go(-1);
			},
			fengmianUploadChange(fileUrls) {
				this.ruleForm.fengmian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");
			},
		}
	};
</script>

