			init(type) {
				this.type = type;
				if(type=='cross'){
					var obj = JSON.parse(localStorage.getItem('crossObj'));
					for (var o in obj){
						if(o=='jiaolianzhanghao'){
							this.ruleForm.jiaolianzhanghao = obj[o];
							this.ro.jiaolianzhanghao = true;
							continue;
						}
						if(o=='jiaolianxingming'){
							this.ruleForm.jiaolianxingming = obj[o];
							this.ro.jiaolianxingming = true;
							continue;
						}
						if(o=='xingbie'){
							this.ruleForm.xingbie = obj[o];
							this.ro.xingbie = true;
							continue;
						}
						if(o=='nianling'){
							this.ruleForm.nianling = obj[o];
							this.ro.nianling = true;
							continue;
						}
						if(o=='tupian'){
							this.ruleForm.tupian = obj[o].split(",")[0];
							this.ro.tupian = true;
							continue;
						}
						if(o=='sijiaofeiyong'){
							this.ruleForm.sijiaofeiyong = obj[o];
							this.ro.sijiaofeiyong = true;
							continue;
						}
						if(o=='shanzhanglingyu'){
							this.ruleForm.shanzhanglingyu = obj[o];
							this.ro.shanzhanglingyu = true;
							continue;
						}
						if(o=='zizhizhengshu'){
							this.ruleForm.zizhizhengshu = obj[o];
							this.ro.zizhizhengshu = true;
							continue;
						}
						if(o=='jiaoxuejingli'){
							this.ruleForm.jiaoxuejingli = obj[o];
							this.ro.jiaoxuejingli = true;
							continue;
