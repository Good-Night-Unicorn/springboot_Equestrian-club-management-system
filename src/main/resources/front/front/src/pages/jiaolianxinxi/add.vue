					tupian : false,
					sijiaofeiyong : false,
					shanzhanglingyu : false,
					zizhizhengshu : false,
					jiaoxuejingli : false,
					gerenjianjie : false,
					gengxinshijian : false,
					clicktime : false,
					clicknum : false,
					discussnum : false,
					storeupnum : false,
				},
				type: '',
				userTableName: localStorage.getItem('UserTableName'),
				ruleForm: {
					jiaolianzhanghao: '',
					jiaolianxingming: '',
					xingbie: '',
					nianling: '',
					tupian: '',
					sijiaofeiyong: '',
					shanzhanglingyu: '',
					zizhizhengshu: '',
					jiaoxuejingli: '',
					gerenjianjie: '',
					gengxinshijian: '',
					clicktime: '',
					clicknum: '',
					discussnum: '',
					storeupnum: '',
				},
				jiaolianzhanghaoOptions: [],


				rules: {
					jiaolianzhanghao: [
					],
					jiaolianxingming: [
					],
